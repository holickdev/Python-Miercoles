# ========================================
# ¿QUÉ ES UNA VARIABLE?
# ========================================

# Una variable tiene un nombre y guarda un valor
edad = 15
nombre = "María"
altura = 1.65

print("Edad:", edad)
print("Nombre:", nombre)
print("Altura:", altura)


# ========================================
# TIPOS DE DATOS
# ========================================

# ENTEROS (int) - Números sin decimales
numero_estudiantes = 30
año = 2026

# DECIMALES (float) - Números con decimales
precio = 19.99
temperatura = 36.5

# TEXTO (str - string) - Cadenas de caracteres
mensaje = "Hola mundo"
ciudad = "Bogotá"

# BOOLEANOS (bool) - Verdadero o Falso
es_estudiante = True
tiene_permiso = False

print("\nEjemplos de tipos de datos:")
print("Entero:", numero_estudiantes)
print("Decimal:", precio)
print("Texto:", mensaje)
print("Booleano:", es_estudiante)


# ========================================
# CAMBIAR EL VALOR DE UNA VARIABLE
# ========================================

puntos = 0
print("Puntos iniciales:", puntos)

puntos = 10
print("Puntos después de ganar:", puntos)

puntos = 25
print("Puntos finales:", puntos)


